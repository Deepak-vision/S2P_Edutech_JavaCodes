package operators;

public class Example3 {

        public static void main(String[] args) {

           // for integer data type
            int a = 12;
            int b = 5;
            int q = a/b;
            int r = a % b;

            System.out.println("To perform division and Modulo Division");
            System.out.println(q);
            System.out.println(r);

            // for float data type
            float lfNum1 = 14.4f;
            float lfNum2 = 3.2f;
            float lfRem  = lfNum1 % lfNum2;  // modulo returns th remainder
            System.out.println(lfRem);



        }
    }
